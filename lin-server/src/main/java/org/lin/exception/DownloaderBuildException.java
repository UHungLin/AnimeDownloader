public class DownloaderBuildException extends Exception {

	public DownloaderBuildException() {
		super();
	}

	public DownloaderBuildException(String message) {
		super(message);
	}

	public DownloaderBuildException(String message, Throwable cause) {
		super(message, cause);
	}

	public DownloaderBuildException(Throwable cause) {
		super(cause);
	}

}
